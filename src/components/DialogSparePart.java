    private void b_addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_b_addActionPerformed
        if (t_nama.getText().isEmpty() || t_price.getText().isEmpty() || t_qty.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please fill all field");
        } else {
            int qty = Integer.parseInt(t_qty.getText());
            int harga = Integer.parseInt(t_price.getText());
            int total = qty * harga;
            f.cud("INSERT into sparepart value (null,'"
                    + t_nama.getText() + "','"
                    + t_price.getText() + "','"
                    + t_qty.getText() + "','"
                    + total + "','" + TeknisiService.id_item1 + "')", "Data berhasil ditambahkan");
            Clear();
            tampil();
            t_total.setText(Integer.toString(getSum()));

        }
    }//GEN-LAST:event_b_addActionPerformed

    private void b_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_b_deleteActionPerformed
        if (id.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Pilih data sparepart terlebih dahulu");
        } else {
            int konfirmasi = JOptionPane.showConfirmDialog(null, "Apakah Anda ingin menghapus", "Hapus", JOptionPane.YES_NO_OPTION);
            if (konfirmasi == 0) {
                f.cud("delete from sparepart where id='" + id
                        + "'", "Data dengan id '" + id + "' berhasil dihapus");
                Clear();
                tampil();
                t_total.setText(Integer.toString(getSum()));

            }
        }
// TODO add your handling code here:
    }//GEN-LAST:event_b_deleteActionPerformed

    private void b_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_b_updateActionPerformed
        if (id.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Pilih data sparepart terlebih dahulu");
        } else if (t_nama.getText().isEmpty() || t_price.getText().isEmpty() || t_qty.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please fill all field");
        } else {
            int qty = Integer.parseInt(t_qty.getText());
            int harga = Integer.parseInt(t_price.getText());
            int total = qty * harga;
            f.cud("update sparepart set name = '" + t_nama.getText()
                    + "', price = '" + t_price.getText()
                    + "', qty='" + t_qty.getText()
                    + "', sub_price = '"
                    + total + "' where id = '" + id
                    + "'", "Data dengan id '" + id + "' berhasil diubah");
            Clear();
            tampil();
            t_total.setText(Integer.toString(getSum()));
        }// TODO add your handling code here:
    }//GEN-LAST:event_b_updateActionPerformed

