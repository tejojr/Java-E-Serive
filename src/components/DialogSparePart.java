    private void tampil() {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("No");
        model.addColumn("ID");
        model.addColumn("Name");
        model.addColumn("Price");
        model.addColumn("Qty");
        model.addColumn("Sub Total");

        try {
            f.select("select * from sparepart where id_item =  '" + TeknisiService.id_item1 + "' ");
            int no = 1;
            while (f.rs.next()) {
                model.addRow(new Object[]{
                    no++,
                    f.rs.getString(1),
                    f.rs.getString(2),
                    f.rs.getString(3),
                    f.rs.getString(4),
                    f.rs.getString(5)
                });

            }
            tabel.setModel(model);
        } catch (Exception e) {
            System.out.println(e);

        }

    }

    private int getSum() {
        int rows = tabel.getRowCount();
        int result = 0;
        if (rows > 0) {
            for (int i = 0; i < rows; i++) {
                result += Integer.parseInt(tabel.getValueAt(i, 5).toString());
            }
        }

        return result;

    }

